#Python statements:
# Instructions that a python interperter can execute are called statements

# statement types: single-line statement & multi-line statements

#single line
i = 5

print(i)

#multi-line statements
# there are two ways to write multi-line statements in python
# first way: by putting backward slash , so that interperter would understand its same statement to continue
i = 1 + 2 + 3 + \
    4 + 5 + 6
print(i)

# second way : by putting inside () round brackets.
i = ( 1 + 2+ 3 +
      4 + 5 + 6)
print(i)



